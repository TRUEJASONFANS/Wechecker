package soot.we.android.callGraph;

import soot.Unit;
import soot.we.android.component.EntityClass;
import soot.we.android.component.EntityMethod;

/**
 * InvokeMethodinfo class is a class to record the the invoke method flow(path)
 * @author jason
 *
 */
public class InvokMethodInfo {
	private EntityClass eclass;
	private EntityMethod emethod;
	private Unit unit;

	public EntityClass getEclass() {
		return eclass;
	}
	public void setEclass(EntityClass eclass) {
		this.eclass = eclass;
	}
	public EntityMethod getEmethod() {
		return emethod;
	}
	public void setEmethod(EntityMethod emethod) {
		this.emethod = emethod;
	}
	public Unit getUnit() {
		return unit;
	}
	public void setUnit(Unit unit) {
		this.unit = unit;
	}
    public InvokMethodInfo(EntityClass eclass,EntityMethod emethod,Unit unit){
    	this.eclass = eclass;
    	this.emethod = emethod;
    	this.unit = unit;
    }
}
